#include "drivers\stm32f4_tim_pwm.h"

#include "beanSproutY.h"
/*====================================================================================================*/
/*====================================================================================================*/
void BSY_Init( void )
{
  SystemInit();
  HAL_InitTick();

  BSY_GPIO_Config();
  BSY_PWM_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void BSY_Loop( void )
{
  uint8_t state = 0;
  uint32_t i = PWM_MIN;

  while(1) {
    
    i = (state) ? (i - 1) : (i + 1);
    if(i == PWM_MAX){ state = 1; }
    if(i == PWM_MIN){ state = 0; }

    TIM_PWM_setDuty(i);

    LED_G_Toggle();
    delay_ms(1);
  }
}
